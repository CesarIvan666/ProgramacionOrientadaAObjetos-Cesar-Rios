package edu.cesarivan666.reto1.process;

public class Calculadora {
    /**
     *
     * suma enteros
     *
     * @param operando1 primer operando para sumar
     * @param operando2 primer operando para sumar
     **/
    public static int sumarNumeros(int operando1, int operando2){
        return operando1+operando2;
    }
    /**
     *
     * resta enteros
     *
     * @param operando1 primer operando para restar
     * @param operando2 primer operando para restar
     **/
    public static int restarNumeros(int operando1, int operando2){
        return operando1-operando2;
    }
    /**
     *
     * multiplicar enteros
     *
     * @param operando1 primer operando para multiplicar
     * @param operando2 primer operando para multiplicar
     **/
    public static int multiplicarNumeros(int operando1, int operando2){
        return operando1*operando2;
    }
    /**
     *
     * resta dividir
     *
     * @param operando1 primer operando dividir
     * @param operando2 primer operando para dividir
     **/
    public static int dividirNumeros(int operando1, int operando2){
        return operando1/operando2;
    }
}
