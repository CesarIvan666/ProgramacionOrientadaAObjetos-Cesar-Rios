package edu.cesarivan666.reto1.process;
public class Calculadora {

    /**
     * suma enteros
     *
     * @param operando1 primer operando para sumar, debe ser tipo entero
     * @param operando2 segundo operando para sumar, debe ser tipo entero
     */
    public static int sumarNumeros(int operando1, int operando2) {
        return operando1 + operando2;
    }

    /**
     * resta enteros
     *
     * @param operando1 primer operando para restar, debe ser tipo entero
     * @param operando2 segundo operando para restar, debe ser tipo entero
     */
    public static int restarNumeros(int operando1, int operando2) {
        return operando1 - operando2;
    }

    /**
     * multiplica enteros
     *
     * @param operando1 primer operando para multiplicar, debe ser tipo entero
     * @param operando2 segundo operando para multiplicar, debe ser tipo entero
     */
    public static int multiplicarNumeros(int operando1, int operando2) {
        return operando1*operando2;
    }

    /**
     * divide enteros
     *
     * @param operando1 primer operando para dividir, debe ser tipo entero
     * @param operando2 segundo operando para dividir, debe ser tipo entero
     */
    public static int divideNumeros(int operando1, int operando2) {
        return operando1/operando2;
    }
}