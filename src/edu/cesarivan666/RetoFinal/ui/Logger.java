package edu.cesarivan666.RetoFinal.ui;

/**

 Esta clase Logger permite imprimir mensajes de registro en la consola.
 */
public class Logger {

    /**
     Este método estático permite imprimir un mensaje en la consola.
     @param mensaje El mensaje que se desea imprimir en la consola.
     **/
    public static void log(String mensaje) {
        System.out.println(mensaje);
    }
}


