package edu.cesarivan666.reto12;
/**

 La interfaz Programer define un método "programar" que debe ser implementado por cualquier clase que la implemente.

 Esta interfaz se utiliza para representar a un programador.
 */
public interface Programer {

    /**

     El método programar debe ser implementado por cualquier clase que implemente la interfaz Programer.
     Este método representa la acción de programar de un programador.
     */
    void programar();
}